package section_07_1_assignment;
// DataTypes Assignment Question -
public class Example1 {
    public static void main(String[] args) {
        // 1. print int value
        int value_1 = 21;
        System.out.println("int value is : "+ value_1);

        //2. print double value
        double value_2 = 9876542.1;
        System.out.println("double value is : "+ value_2 );

        // 3. print character
        char char_value = 'A';
        System.out.println("char value is : "+ char_value);

        // 4. boolean value
        boolean isAlive = true;
        System.out.println("You are Alive : "+ isAlive);

        //5. float value
        float enter_percentage = 88.2f;
        System.out.println("Your Percentage is : "+enter_percentage + "%");

        // 6. Integer Wrapper class - creating Integer object
        Integer Integer_value = 122;
        System.out.println("Integer object value is : "+ Integer_value);

        // 7. Double wrapper class - crating Double object
        Double dec_num = 90999.99;
        System.out.println("Double object value is : "+ dec_num);

        // 8. Character Wrapper class
        Character char_1 = 'B';
        System.out.println("Character object value is : "+ char_1);

        //9. Boolean Wrapper class -
        Boolean isPassed = false ;
        System.out.println("are you passed : "+ isPassed);
    }
}
